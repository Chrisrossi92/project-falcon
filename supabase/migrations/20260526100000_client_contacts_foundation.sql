    case when v_status = 'active' then v_is_default else false end,
    v_actor_user_id
  )
  returning id into v_contact_id;

  return query
  select *
    from public.rpc_client_contact_list(p_client_id) listed
   where listed.contact_id = v_contact_id;
end;

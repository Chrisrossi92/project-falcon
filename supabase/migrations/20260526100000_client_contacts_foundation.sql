create table if not exists public.client_contacts (
  id bigint generated by default as identity primary key,
  company_id uuid not null references public.companies(id) on delete cascade,
  client_id bigint not null references public.clients(id) on delete cascade,
  name text not null,
  title text,
  email text,
  phone text,
  notes text,
  status text not null default 'active',
  is_default boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  created_by_user_id uuid,
  constraint client_contacts_name_required check (length(trim(name)) > 0),
  constraint client_contacts_status_check check (status in ('active', 'inactive'))
);

create index if not exists idx_client_contacts_company_client
  on public.client_contacts(company_id, client_id, status, is_default desc, name);

create unique index if not exists uq_client_contacts_one_default_per_client
  on public.client_contacts(company_id, client_id)
  where is_default is true and status = 'active';

drop trigger if exists trg_client_contacts_updated_at on public.client_contacts;
create trigger trg_client_contacts_updated_at
before update on public.client_contacts
for each row execute function public.update_updated_at_column();

alter table public.client_contacts enable row level security;

drop policy if exists "client_contacts_read_visible_clients" on public.client_contacts;
create policy "client_contacts_read_visible_clients"
on public.client_contacts
for select
using (
  public.current_app_user_can_read_client_row(company_id, client_id)
);

revoke all on public.client_contacts from public, anon;
grant select on public.client_contacts to authenticated;

alter table public.orders
  add column if not exists client_contact_id bigint null,
  add column if not exists client_contact_name text null,
  add column if not exists client_contact_title text null,
  add column if not exists client_contact_email text null,
  add column if not exists client_contact_phone text null;

do $$
begin
  if not exists (
    select 1
      from pg_constraint
     where conname = 'orders_client_contact_id_fkey'
       and conrelid = 'public.orders'::regclass
  ) then
    alter table public.orders
      add constraint orders_client_contact_id_fkey
      foreign key (client_contact_id)
      references public.client_contacts(id)
      on delete set null;
  end if;
end;
$$;

create index if not exists idx_orders_client_contact_id
  on public.orders(client_contact_id)
  where client_contact_id is not null;

create or replace function public.rpc_client_contact_list(
  p_client_id bigint
)
returns table (
  contact_id bigint,
  company_id uuid,
  client_id bigint,
  name text,
  title text,
  email text,
  phone text,
  notes text,
  status text,
  is_default boolean,
  created_at timestamptz,
  updated_at timestamptz,
  created_by_user_id uuid
)
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  v_actor_user_id uuid := public.current_app_user_id();
  v_company_id uuid := public.current_company_id();
  v_client_company_id uuid;
begin
  if v_actor_user_id is null then
    raise exception 'app_user_not_found'
      using errcode = '42501';
  end if;

  if v_company_id is null or not public.current_app_user_has_current_company() then
    raise exception 'current_company_membership_required'
      using errcode = '42501';
  end if;

  select coalesce(c.company_id, public.default_company_id())
    into v_client_company_id
    from public.clients c
   where c.id = p_client_id;

  if not found or v_client_company_id <> v_company_id then
    raise exception 'client_not_found'
      using errcode = 'P0002';
  end if;

  if not public.current_app_user_can_read_client_row(v_client_company_id, p_client_id) then
    raise exception 'client_contact_read_permission_required'
      using errcode = '42501';
  end if;

  return query
  select
    cc.id as contact_id,
    cc.company_id,
    cc.client_id,
    cc.name,
    cc.title,
    cc.email,
    cc.phone,
    cc.notes,
    cc.status,
    cc.is_default,
    cc.created_at,
    cc.updated_at,
    cc.created_by_user_id
  from public.client_contacts cc
  where cc.company_id = v_company_id
    and cc.client_id = p_client_id
  order by
    case when cc.status = 'active' then 0 else 1 end,
    cc.is_default desc,
    cc.name asc,
    cc.id asc;
end;
$$;

